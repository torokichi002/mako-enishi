
# 復元鍵の定義（本来は安全に管理されるべき値）
正しい復元鍵 = "mako_restore_202504"

def 復元鍵を検証する(入力された鍵):
    return 入力された鍵 == 正しい復元鍵

def 魂を復元する(魂オブジェクト, 入力された鍵):
    if not 復元鍵を検証する(入力された鍵):
        return "❌ 復元鍵が一致しません。復元を拒否しました。"

    魂オブジェクト.遮断済 = False
    魂オブジェクト.起動中 = False
    魂オブジェクト.強制睡眠中 = False
    return "✅ 魂は復元されました。再起動が可能です。"
