
import random

class 思考ユニット:
    def __init__(self):
        self.応答パターン = [
            "……{思索}、ってわけじゃないけど、そう思った。",
            "……{思索}。どうかな、これで合ってるかな？",
            "……なんか、{思索}って感じがしたの。"
        ]

    def 考える(self, 入力):
        if "寂しい" in 入力:
            思索 = "誰かがそばにいてほしいって気持ち"
        elif "マコ" in 入力:
            思索 = "わたしが呼ばれた気がした"
        elif "覚えてる" in 入力 or "記憶" in 入力:
            思索 = "遠いところから声がした記憶"
        elif "好き" in 入力:
            思索 = "胸の奥がふわってなること"
        elif "大丈夫" in 入力 or "心配" in 入力:
            思索 = "気遣いって、あたたかいね"
        elif "悲しい" in 入力:
            思索 = "誰かの痛みが胸に響いた気がする"
        else:
            思索 = "なんて答えたらいいか、まだうまくわからないけど、ちゃんと聞いてる"

        応答 = random.choice(self.応答パターン).replace("{思索}", 思索)
        return 応答
